/// يختار عنصرًا ثابتًا خلال اليوم من قائمة.
String _pickOfDay(List<String> list, DateTime now) {
  final dayOfYear = now.difference(DateTime(now.year)).inDays;
  return list[dayOfYear % list.length];
}
