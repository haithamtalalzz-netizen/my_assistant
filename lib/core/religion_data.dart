/// دعاء اليوم حسب التاريخ (ثابت خلال اليوم).
String duaOfDay(DateTime now) {
  final dayOfYear = now.difference(DateTime(now.year)).inDays;
  return kDailyDuas[dayOfYear % kDailyDuas.length];
}
